bool check(int n){
    int used[10] = {0};
    while(n > 0){
        if(used[n%10]) return false;
        used[n%10]++;
        n /= 10;
    }
    return true;
}

int main() {
    int n;
    cin >> n;
    n++;
    for(;;n++){
        if(check(n)){
            cout << n << endl;
            break;
        }
    }
    
    return 0;
}
